$(document).ready(function(){
    // retorna a largura de um elemento
    $('.paragrafo').width()

    // retorna a largura de um elemento
    // levando em conta o padding!
    $('.paragrafo').innerWidth()

    // define a largura de um elemento
    $('.paragrafo').width(300)

    
    // retorna a altura de um elemento
    $('.paragrafo').height()

    // retorna a altura de um elemento
    // levando em conta o padding!
    $('.paragrafo').innerHeight()

    // define a altura de um elemento
    $('.paragrafo').height(120)
});